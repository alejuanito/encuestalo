package com.encuestas.data.bean;


public class AtencionBean {
	private Integer idAtencion;	
	private Integer nuMesa;
	private ColaboradorBean colaborador;
	private AreaBean area;
	public Integer getIdAtencion() {
		return idAtencion;
	}
	public void setIdAtencion(Integer idAtencion) {
		this.idAtencion = idAtencion;
	}
	public Integer getNuMesa() {
		return nuMesa;
	}
	public void setNuMesa(Integer nuMesa) {
		this.nuMesa = nuMesa;
	}
	public ColaboradorBean getColaborador() {
		return colaborador;
	}
	public void setColaborador(ColaboradorBean colaborador) {
		this.colaborador = colaborador;
	}
	public AreaBean getArea() {
		return area;
	}
	public void setArea(AreaBean area) {
		this.area = area;
	}
	

	
	
	
	
}
